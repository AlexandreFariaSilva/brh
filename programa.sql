create or replace FUNCTION     CALCULA_IDADE 
(C_DATA_CALC IN DATE)
RETURN FLOAT
IS
    A_IDADE FLOAT;
BEGIN
    A_IDADE := FLOOR(MONTHS_BETWEEN(SYSDATE, C_DATA_CALC) / 12);
    RETURN A_IDADE;
END;